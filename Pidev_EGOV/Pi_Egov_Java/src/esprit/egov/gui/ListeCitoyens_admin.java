    void updateTable() {
        DefaultTableModel model = (DefaultTableModel) DemandesCin.getModel();
        model.setRowCount(0);
        IUtilisateurDao utiDao = new UtilisateurDao();
        List<Utilisateur> listeCin = utiDao.findAllCin();
        for (Utilisateur ind : listeCin) {
            model.addRow(
                    new Object[]{ind.getIduti(),ind.getNumCin() ,ind.getNom(), ind.getPrenom(), ind.getDateNaissance()
                               , ind.getLieuNaissance(), ind.getNomPere(), ind.getPrenomPere()
                               , ind.getNomMere(), ind.getPrenomMere(), ind.getSexe()
                               , ind.getStatutSocial(), ind.getAdresse(), ind.getProfession()
                    }
            );
        }
    }

