    private void DemandesCinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DemandesCinMouseClicked
        int row = DemandesCin.getSelectedRow();
        NumCin.setText(DemandesCin.getModel().getValueAt(row, 1).toString());
        String nom = (DemandesCin.getModel().getValueAt(row, 2).toString());
        Nom.setText(nom);
        String pnom = (DemandesCin.getModel().getValueAt(row, 3).toString());
        Prenom.setText(pnom);
        Date dateNaiss = (Date) DemandesCin.getModel().getValueAt(row, 4);
        DateNaiss.setDate(dateNaiss);
        String Lieu = (DemandesCin.getModel().getValueAt(row, 5).toString());
        LieuNaiss.setText(Lieu);
        String nomP = (DemandesCin.getModel().getValueAt(row, 6).toString());
        NomPére.setText(nomP);
        String pnomP = (DemandesCin.getModel().getValueAt(row, 7).toString());
        PrenomPére.setText(pnomP);
        String nomM = (DemandesCin.getModel().getValueAt(row, 8).toString());
        NomMère.setText(nomM);
        String pnomM = (DemandesCin.getModel().getValueAt(row, 9).toString());
        PrenomMère1.setText(pnomM);
        String sexe = (DemandesCin.getModel().getValueAt(row, 10).toString());
        if (sexe.equalsIgnoreCase("Feminin") || sexe.equalsIgnoreCase("Feminin")) {
            Feminin.setSelected(true);
        }
        if (sexe.equalsIgnoreCase("Masculin") || sexe.equalsIgnoreCase("masculin")) {
            Masculin.setSelected(true);
        }
        String statutSocial = DemandesCin.getModel().getValueAt(row, 11).toString();
        statutsocial.setText(statutSocial);
        String adr = DemandesCin.getModel().getValueAt(row, 12).toString();
        Adresse.setText(adr);
        String prof = DemandesCin.getModel().getValueAt(row, 13).toString();
        Profession.setText(prof);
        IUtilisateurDao utiDao = new UtilisateurDao();
        Image imageUti = utiDao.findUtiPhotoByid((int) DemandesCin.getModel().getValueAt(row, 0));
        Image im = imageUti.getScaledInstance(photoLabel.getWidth(), photoLabel.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon newImage = new ImageIcon(im);
        photoLabel.setIcon(newImage);
    }//GEN-LAST:event_DemandesCinMouseClicked

