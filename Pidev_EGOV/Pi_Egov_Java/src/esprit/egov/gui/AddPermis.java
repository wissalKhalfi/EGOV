    private void validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerActionPerformed
        Permis permis = new Permis();
        PermisDao permisDao = new PermisDao();
        //Integer integer=permis.getNumPermis();
        //permis.setNumPermis(numPermis.getText());
       // permis.setNumPermis(integer.persistfaza(numPermis.getText()));
          if (numPermis.getText().isEmpty() ){
           JOptionPane.showMessageDialog(jLabel1, "les champs sont vides");
        }
           Pattern pattern = Pattern.compile("^[0-9]{8}$");
        Matcher m = pattern.matcher(numPermis.getText());

        boolean b = m.matches();
        if (b) {
            int num_carte = Integer.parseInt(numPermis.getText());
        } else { JOptionPane.showMessageDialog(jLabel1,"le code permis doit contenir 8 chiffres!");
        }
        permis.setNumPermis(Integer.parseInt(numPermis.getText()));
        
        permis.setDate_creation_permis(jDateChooser2.getDate());
        
        if(Feminin.isSelected()){
           permis.setCategorie_permis(Feminin.getText()); 
        }
        if(Masculin.isSelected()){
           permis.setCategorie_permis(Masculin.getText()); 
        }
        
        permis.setDate_validite_permis(jDateChooser1.getDate());
        
        IUtilisateurDao utiDao = new UtilisateurDao() ;
       
        permisDao.addPermis(permis);
        try {
            new SendMail(mailtf.getText(),"Ajout Permis","Permis ajout√© avec succes !");
        } catch (AddressException ex) {
            Logger.getLogger(AddPermis.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_validerActionPerformed

