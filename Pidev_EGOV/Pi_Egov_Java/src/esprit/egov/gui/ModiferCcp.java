    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
   try {
            CcpDao cd = new CcpDao();
            Ccp carte = new Ccp();
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            java.util.Date date =  format.parse(jTextField6.getText());
            java.util.Date date2 =  format.parse(jTextField7.getText());
            java.sql.Date sqlDate = new java.sql.Date(date.getTime());
            java.sql.Date sqlDate2 = new java.sql.Date(date2.getTime());
            carte.setNum_ccp(Integer.parseInt(jTextField2.getText()));
            carte.setCode_ccp(Integer.parseInt(jTextField4.getText()));
            carte.setSolde_actuel(Integer.parseInt(jTextField5.getText()));
            carte.setDate_creation(sqlDate);
            carte.setDate_dernier_versement(sqlDate2);
            carte.setDernier_versement(Integer.parseInt(jTextField8.getText()));
            cd.modifyCcp(carte);
        } catch (ParseException ex) {
            Logger.getLogger(Modifieredinar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

