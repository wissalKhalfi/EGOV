    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       ArticlesDao cd = new ArticlesDao();
        Articles carte = new Articles();
        int m = Integer.parseInt(jTextField2.getText());
        carte = cd.findArticlesid(m);
        String n = String.valueOf(carte.getId_article());
        String c = String.valueOf(carte.getTitre_article());
        String s = String.valueOf(carte.getType());
        String[] t = {n, c, s, "supprimer"};
        model.addRow(t);
        jTable1.getColumn("action").setCellRenderer(new ButtonRender());
        jTable1.getColumn("action").setCellEditor(new DeletebuttonEditor(new JTextField()));
    }//GEN-LAST:event_jButton1ActionPerformed

