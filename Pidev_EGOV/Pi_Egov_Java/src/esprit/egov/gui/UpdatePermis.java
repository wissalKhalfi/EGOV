    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        PermisDao permisDao = new PermisDao();
       Permis permis=new Permis();
        //b.setDate_creation_permis(Integer.parseInt(date_creation_permis.getText()));
        System.out.println(date_creation_permis.getDate());
        permis.setDate_creation_permis(date_creation_permis.getDate());
       // b.setCategorie_permis(Integer.parseInt(categorie_permis.getText()));
        if(Feminin.isSelected()){
           permis.setCategorie_permis(Feminin.getText()); 
        }
        if(Masculin.isSelected()){
           permis.setCategorie_permis(Masculin.getText()); 
        }
         permis.setDate_validite_permis(jDateChooser1.getDate());
        

        permisDao.updatePermis(permis);
        
    }//GEN-LAST:event_UpdateActionPerformed

 
    
    
