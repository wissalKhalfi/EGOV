    void updateTable() {
        DefaultTableModel model = (DefaultTableModel) DemandesEN.getModel();
        IUtilisateurDao utiDao = new UtilisateurDao();
        model.setRowCount(0);
        List<Utilisateur> listeEN = utiDao.findAllEN();
        for (Utilisateur ind : listeEN) {
            model.addRow(
                    new Object[]{ind.getIduti(), ind.getNom(), ind.getPrenom(), ind.getDateNaissance(), ind.getLieuNaissance(), ind.getNomPere(), ind.getPrenomPere(), ind.getNomMere(), ind.getPrenomMere(), ind.getSexe()
                    }
            );
        }
    }

    void updateTable(String nom) {
        DefaultTableModel model = (DefaultTableModel) DemandesEN.getModel();
        IUtilisateurDao utiDao = new UtilisateurDao();
        model.setRowCount(0);
        List<Utilisateur> listeExtNaiss = utiDao.findUtilisateurByNom(nom);
        listeExtNaiss.stream().forEach((ind) -> {
            model.addRow(
                    new Object[]{ind.getIduti(), ind.getNom(), ind.getPrenom(), ind.getDateNaissance(), ind.getLieuNaissance(), ind.getNomPere(), ind.getPrenomPere(), ind.getNomMere(), ind.getPrenomMere(), ind.getSexe()
                    }
            );
        });
    }

    void updateTable(String nom, String prenom, Date dateNaiss) {
        IUtilisateurDao utiDao = new UtilisateurDao();
        List<Utilisateur> listeEN = utiDao.findUtilisateurByNomPrenomDateNaiss(nom, prenom, dateNaiss);

        DefaultTableModel model = (DefaultTableModel) DemandesEN.getModel();
        for (Utilisateur ind : listeEN) {
            model.addRow(
                    new Object[]{ind.getNom(), ind.getPrenom(), ind.getDateNaissance(), ind.getLieuNaissance(), ind.getNomPere(), ind.getPrenomPere(), ind.getNomMere(), ind.getPrenomMere(), ind.getSexe()
                    }
            );
        }

    }

