    private void NomRechKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NomRechKeyReleased

        String nom = NomRech.getText();
        if (nom.isEmpty()) {
            updateTable();
        }
        if (!nom.isEmpty()) {
            updateTable(nom);
        }

    }//GEN-LAST:event_NomRechKeyReleased

    private void ImprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimerActionPerformed
        MessageFormat header = new MessageFormat("Liste d'extraits de naissance \n Nom: " + NomRech.getText() + "\n Prénom: " + Pnom + "\n Date de naissance:\n" + DateNaiss.getDateFormatString() + "\n\n");
        MessageFormat footer = new MessageFormat("Page{0,number,integer}");
        try {
            DemandesEN.print(JTable.PrintMode.FIT_WIDTH, header, footer);
        } catch (java.awt.print.PrinterException e) {
            System.err.format("Erreur lors de l'impression ", e.getMessage());
        }
    }//GEN-LAST:event_ImprimerActionPerformed

