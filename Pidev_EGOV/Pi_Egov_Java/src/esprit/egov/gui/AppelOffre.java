    private void jBtUpdateAppelOffreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtUpdateAppelOffreActionPerformed
try {
            int row = jTableAppelOffre.getSelectedRow();
            Object idAppelOffre = jTableAppelOffre.getValueAt(row, 0);
            Appel_offre appeloffre = new Appel_offre();
            appeloffre.setObjet(jTfobjet.getText());
            appeloffre.setDate_dep(jDatChDeb.getDate());
            appeloffre.setDate_fin(jDatChfin.getDate());
            appeloffre.setLocalisation(jTflocalisation.getText());
            appeloffre.setProfil_demander(jTfprofil.getText());
            appeloffre.setId_appel(Integer.valueOf(idAppelOffre.toString()));
            IAppel_offreDao applOff = new Appel_offreDao();
            applOff.updateAppel_offre(appeloffre);
            //jTableAppelOffre.refresh(appeloffre);
            JOptionPane.showMessageDialog(null, "modification avec succ√©e !");
                        jTableAppelOffre.setModel(new AppelOffreAdapter());

jScrollPane1.setViewportView(jTableAppelOffre);
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jBtUpdateAppelOffreActionPerformed

